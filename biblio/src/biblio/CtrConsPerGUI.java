    private void valider_buttonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_valider_buttonMousePressed
      // TODO add your handling code here:
      try {
          String numero = numero_textfield.getText();
          Periodique per = biblio.unPeriodique(numero);
          if (per != null) {
            this.dispose();
            // Création de la vue
            VuePeriodiqueGUI vPerGUI = new VuePeriodiqueGUI(parent_frame, true);
            vPerGUI.menuPrincipal(per);
             // Suppression de la vue vLect comme Observatrice de l'objet lect
            vPerGUI.elimineObserveur();
            vPerGUI = null;  // suppression de la vue
          } else  {
            notif_jlabel.setForeground(Color.red);
            notif_jlabel.setText("Le périodique n'existe pas dans la base.");

          }
      } catch(java.lang.NumberFormatException Ex) {
          notif_jlabel.setForeground(Color.red);
          notif_jlabel.setText("Saisissez un numéro ISSN de périodique.");
      }
    }//GEN-LAST:event_valider_buttonMousePressed

